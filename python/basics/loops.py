#!/usr/bin/env python3

if __name__ == "__main__":

    n = 1
    while n < 5:
        print(n)
        n += 1

    # Looping from 0 to 4
    for i in range(0, 5, 1):
        print(i)

    for i in range(5):
        print(i)

    # Looping from 4 to 0
    for i in range(4, -1, -1):
        print(i)
